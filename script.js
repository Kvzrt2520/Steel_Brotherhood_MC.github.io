// ==================== 1. –§–£–ù–ö–¶–ò–Ø –ü–†–ò–í–ï–¢–°–¢–í–ò–Ø –ü–û –í–†–ï–ú–ï–ù–ò –°–£–¢–û–ö ====================

// –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –≤—Ä–µ–º–µ–Ω–∏ —Å—É—Ç–æ–∫
function showWelcomeMessage() {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–∞—Ö–æ–¥–∏–º—Å—è –ª–∏ –º—ã –Ω–∞ –≥–ª–∞–≤–Ω–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü–µ
    // window.location.pathname - –ø—É—Ç—å —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    // includes('index.html') - –ø—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –≤ –ø—É—Ç–∏ "index.html"
    // === '/' - –ø—Ä–æ–≤–µ—Ä—è–µ–º, —ç—Ç–æ –∫–æ—Ä–Ω–µ–≤–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞
    // endsWith('.html') === false - –ø—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ—Ç –ª–∏ .html –≤ –∫–æ–Ω—Ü–µ (–º–æ–∂–µ—Ç –±—ã—Ç—å –≥–ª–∞–≤–Ω–∞—è –±–µ–∑ .html)
    if (window.location.pathname.includes('index.html') ||
        window.location.pathname === '/' ||
        window.location.pathname.endsWith('.html') === false) {

        // –°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π div —ç–ª–µ–º–µ–Ω—Ç –¥–ª—è –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è
        const welcomeDiv = document.createElement('div');
        // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º id –¥–ª—è —Å–æ–∑–¥–∞–Ω–Ω–æ–≥–æ div
        welcomeDiv.id = 'welcome-message';
        // –î–æ–±–∞–≤–ª—è–µ–º CSS –∫–ª–∞—Å—Å –¥–ª—è —Å—Ç–∏–ª–∏–∑–∞—Ü–∏–∏
        welcomeDiv.className = 'welcome-message';

        // –°–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç Date –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ –≤—Ä–µ–º–µ–Ω–∏
        const now = new Date();
        // –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–π —á–∞—Å (0-23)
        const hour = now.getHours();
        // –ü–æ–ª—É—á–∞–µ–º —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–π —è–∑—ã–∫ –∏–∑ localStorage –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º 'ru' –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
        const currentLang = localStorage.getItem('siteLanguage') || 'ru';

        // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è
        let greeting = '';
        // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–ª—é—á–∞ –ø–µ—Ä–µ–≤–æ–¥–∞
        let greetingKey = '';

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –≤—Ä–µ–º—è —Å—É—Ç–æ–∫ –∏ –≤—ã–±–∏—Ä–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ
        if (hour >= 5 && hour < 12) {
            // –£—Ç—Ä–æ: —Å 5:00 –¥–æ 11:59
            greetingKey = '–î–æ–±—Ä–æ–µ —É—Ç—Ä–æ, –±–∞–π–∫–µ—Ä! ‚òÄÔ∏è';
        } else if (hour >= 12 && hour < 18) {
            // –î–µ–Ω—å: —Å 12:00 –¥–æ 17:59
            greetingKey = '–î–æ–±—Ä—ã–π –¥–µ–Ω—å, –±—Ä–∞—Ç! üèçÔ∏è';
        } else if (hour >= 18 && hour < 23) {
            // –í–µ—á–µ—Ä: —Å 18:00 –¥–æ 22:59
            greetingKey = '–î–æ–±—Ä—ã–π –≤–µ—á–µ—Ä, –Ω–∞–µ–∑–¥–Ω–∏–∫! üåá';
        } else {
            // –ù–æ—á—å: —Å 23:00 –¥–æ 4:59
            greetingKey = '–î–æ–±—Ä–æ–π –Ω–æ—á–∏, —Å—Ç—Ä–∞–Ω–Ω–∏–∫! üåô';
        }

        // –ü–æ–ª—É—á–∞–µ–º –ø–µ—Ä–µ–≤–æ–¥ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª, –µ—Å–ª–∏ –ø–µ—Ä–µ–≤–æ–¥–∞ –Ω–µ—Ç
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º: –µ—Å—Ç—å –ª–∏ –ø–µ—Ä–µ–≤–æ–¥—ã –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ —è–∑—ã–∫–∞ –ò –µ—Å—Ç—å –ª–∏ –ø–µ—Ä–µ–≤–æ–¥ –¥–ª—è greetingKey
        greeting = translations[currentLang] && translations[currentLang][greetingKey]
            ? translations[currentLang][greetingKey]  // –ë–µ—Ä—ë–º –ø–µ—Ä–µ–≤–æ–¥
            : greetingKey;  // –ò–ª–∏ –æ—Å—Ç–∞–≤–ª—è–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª

        // –í—Å—Ç–∞–≤–ª—è–µ–º HTML —Å –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ–º –≤ —Å–æ–∑–¥–∞–Ω–Ω—ã–π div
        welcomeDiv.innerHTML = `<div class="welcome-text">${greeting}</div>`;
        // –î–æ–±–∞–≤–ª—è–µ–º div –≤ —Å–∞–º–æ–µ –Ω–∞—á–∞–ª–æ body —Å—Ç—Ä–∞–Ω–∏—Ü—ã
        document.body.prepend(welcomeDiv);

        // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ç–∞–π–º–µ—Ä –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è —á–µ—Ä–µ–∑ 5 —Å–µ–∫—É–Ω–¥
        setTimeout(() => {
            // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —ç–ª–µ–º–µ–Ω—Ç –≤—Å—ë –µ—â—ë —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
            if (welcomeDiv.parentNode) {
                // –£–¥–∞–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç —Å–æ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
                welcomeDiv.remove();
            }
        }, 5000);  // 5000 –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥ = 5 —Å–µ–∫—É–Ω–¥
    }
}

// ==================== 2. –§–£–ù–ö–¶–ò–ò –î–õ–Ø –ß–ê–°–û–í –ù–ê –°–¢–†–ê–ù–ò–¶–ï ====================

// –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –Ω–∞—Å—Ç—Ä–∞–∏–≤–∞–µ—Ç —á–∞—Å—ã: —Å–∫—Ä—ã—Ç–∏–µ/–ø–æ–∫–∞–∑–∞–Ω–∏–µ –∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏
function setupClock() {
    // –ù–∞—Ö–æ–¥–∏–º –∫–Ω–æ–ø–∫—É –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —á–∞—Å–æ–≤ –ø–æ id
    const toggleBtn = document.getElementById('timeToggleBtn');
    // –ù–∞—Ö–æ–¥–∏–º –±–ª–æ–∫ —Å —á–∞—Å–∞–º–∏ –ø–æ id
    const clockWidget = document.getElementById('clock-widget');

    // –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ —ç–ª–µ–º–µ–Ω—Ç—ã, –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏
    if (!toggleBtn || !clockWidget) return;

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª–∏ –ª–∏ —á–∞—Å—ã —Å–∫—Ä—ã—Ç—ã –ø—Ä–∏ –ø—Ä–æ—à–ª–æ–º –ø–æ—Å–µ—â–µ–Ω–∏–∏
    // localStorage.getItem('clockHidden') –ø–æ–ª—É—á–∞–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    // === 'true' –ø—Ä–æ–≤–µ—Ä—è–µ–º, —Ä–∞–≤–Ω–æ –ª–∏ –æ–Ω–æ —Å—Ç—Ä–æ–∫–µ 'true'
    const isHidden = localStorage.getItem('clockHidden') === 'true';
    // –ï—Å–ª–∏ —á–∞—Å—ã –±—ã–ª–∏ —Å–∫—Ä—ã—Ç—ã, –¥–æ–±–∞–≤–ª—è–µ–º –∫–ª–∞—Å—Å 'hidden' –¥–ª—è –∏—Ö —Å–∫—Ä—ã—Ç–∏—è
    if (isHidden) {
        clockWidget.classList.add('hidden');
    }

    // –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è 'click' –Ω–∞ –∫–Ω–æ–ø–∫—É –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è
    toggleBtn.addEventListener('click', function() {
        // –ü–µ—Ä–µ–∫–ª—é—á–∞–µ–º –∫–ª–∞—Å—Å 'hidden' —É –±–ª–æ–∫–∞ —á–∞—Å–æ–≤ (–¥–æ–±–∞–≤–ª—è–µ–º/—É–±–∏—Ä–∞–µ–º)
        clockWidget.classList.toggle('hidden');
        // –°–æ—Ö—Ä–∞–Ω—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ —á–∞—Å–æ–≤ –≤ localStorage
        // –ï—Å–ª–∏ –∫–ª–∞—Å—Å 'hidden' –µ—Å—Ç—å, —Å–æ—Ö—Ä–∞–Ω—è–µ–º 'true', –∏–Ω–∞—á–µ 'false'
        localStorage.setItem('clockHidden', clockWidget.classList.contains('hidden'));
    });

    // –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –≤—Ä–µ–º–µ–Ω–∏ —Å—Ä–∞–∑—É
    updateClock();
    // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –≤—Ä–µ–º–µ–Ω–∏ –∫–∞–∂–¥—É—é —Å–µ–∫—É–Ω–¥—É (1000 –º—Å)
    setInterval(updateClock, 1000);
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –≤—Ä–µ–º–µ–Ω–∏ –≤ —á–∞—Å–∞—Ö
function updateClock() {
    // –°–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç —Å —Ç–µ–∫—É—â–µ–π –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º
    const now = new Date();

    // –ü–æ–ª—É—á–∞–µ–º —á–∞—Å—ã, —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –≤ 2 —Ü–∏—Ñ—Ä—ã —Å –≤–µ–¥—É—â–∏–º –Ω—É–ª—ë–º
    const hours = String(now.getHours()).padStart(2, '0');
    // –ü–æ–ª—É—á–∞–µ–º –º–∏–Ω—É—Ç—ã, —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –≤ 2 —Ü–∏—Ñ—Ä—ã —Å –≤–µ–¥—É—â–∏–º –Ω—É–ª—ë–º
    const minutes = String(now.getMinutes()).padStart(2, '0');
    // –ü–æ–ª—É—á–∞–µ–º —Å–µ–∫—É–Ω–¥—ã, —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –≤ 2 —Ü–∏—Ñ—Ä—ã —Å –≤–µ–¥—É—â–∏–º –Ω—É–ª—ë–º
    const seconds = String(now.getSeconds()).padStart(2, '0');
    // –°–æ–±–∏—Ä–∞–µ–º –≤—Ä–µ–º—è –≤ —Å—Ç—Ä–æ–∫—É —Ñ–æ—Ä–º–∞—Ç–∞ "–ß–ß:–ú–ú:–°–°"
    const timeString = `${hours}:${minutes}:${seconds}`;

    // –ü–æ–ª—É—á–∞–µ–º –¥–µ–Ω—å –º–µ—Å—è—Ü–∞
    const day = now.getDate();
    // –ü–æ–ª—É—á–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –º–µ—Å—è—Ü–∞ –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ
    const month = now.toLocaleString('ru-RU', { month: 'long' });
    // –ü–æ–ª—É—á–∞–µ–º –≥–æ–¥
    const year = now.getFullYear();
    // –°–æ–±–∏—Ä–∞–µ–º –¥–∞—Ç—É –≤ —Å—Ç—Ä–æ–∫—É —Ñ–æ—Ä–º–∞—Ç–∞ "–¥–µ–Ω—å –º–µ—Å—è—Ü –≥–æ–¥"
    const dateString = `${day} ${month} ${year}`;

    // –ù–∞—Ö–æ–¥–∏–º —ç–ª–µ–º–µ–Ω—Ç –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤—Ä–µ–º–µ–Ω–∏
    const timeElement = document.getElementById('current-time');
    // –ù–∞—Ö–æ–¥–∏–º —ç–ª–µ–º–µ–Ω—Ç –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –¥–∞—Ç—ã
    const dateElement = document.getElementById('current-date');

    // –ï—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç –≤—Ä–µ–º–µ–Ω–∏ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –æ–±–Ω–æ–≤–ª—è–µ–º –µ–≥–æ —Ç–µ–∫—Å—Ç
    if (timeElement) timeElement.textContent = timeString;
    // –ï—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç –¥–∞—Ç—ã —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –æ–±–Ω–æ–≤–ª—è–µ–º –µ–≥–æ —Ç–µ–∫—Å—Ç
    if (dateElement) dateElement.textContent = dateString;
}

