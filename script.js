function translatePage(lang) {
    // –ù–∞—Ö–æ–¥–∏–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ, —É –∫–æ—Ç–æ—Ä—ã—Ö –µ—Å—Ç—å –∞—Ç—Ä–∏–±—É—Ç data-translate
    const elements = document.querySelectorAll('[data-translate]');

    // –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –∫–∞–∂–¥–æ–º—É –Ω–∞–π–¥–µ–Ω–Ω–æ–º—É —ç–ª–µ–º–µ–Ω—Ç—É
    elements.forEach(element => {
        // –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –∞—Ç—Ä–∏–±—É—Ç–∞ data-translate (—ç—Ç–æ –∫–ª—é—á –ø–µ—Ä–µ–≤–æ–¥–∞)
        const key = element.getAttribute('data-translate');

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –ø–µ—Ä–µ–≤–æ–¥ –¥–ª—è –¥–∞–Ω–Ω–æ–≥–æ —è–∑—ã–∫–∞ –∏ –∫–ª—é—á–∞
        if (translations[lang] && translations[lang][key]) {
            // –ï—Å–ª–∏ –µ—Å—Ç—å, –∑–∞–º–µ–Ω—è–µ–º —Ç–µ–∫—Å—Ç —ç–ª–µ–º–µ–Ω—Ç–∞ –Ω–∞ –ø–µ—Ä–µ–≤–æ–¥
            element.textContent = translations[lang][key];
        }
    });

    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–π —è–∑—ã–∫ –≤ localStorage
    localStorage.setItem('siteLanguage', lang);

    // –ù–∞—Ö–æ–¥–∏–º –∫–Ω–æ–ø–∫—É –ø–µ—Ä–µ–≤–æ–¥–∞
    const translateBtn = document.getElementById('translateBtn');
    // –ï—Å–ª–∏ –∫–Ω–æ–ø–∫–∞ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –º–µ–Ω—è–µ–º –µ—ë —Ç–µ–∫—Å—Ç (RU/EN)
    if (translateBtn) {
        translateBtn.textContent = lang === 'ru' ? 'RU' : 'EN';
    }
}

